地方差[編集]
餅米を利用した米菓は、小さい形状が多いあられ（粒状のもの）、あられより大きい位おかき、その他は揚げ餅と分けられる[1]。関西ではおかきやあられ屋があり、それらの店で販売しているなど地域差がある。
小麦粉、卵などを原料にするもの、馬鈴薯などのでん粉を用いるもの、別の材料を用いたり等の、類似の外観や食感を持つものも煎餅と呼ぶ場合がある。小麦粉を原料とするものは、主に関西で古くから作られている。材料は主に小麦粉、砂糖、卵などで、カステラやビスケットに近く、味は甘めのものが多い。そのため甘味煎餅（あまみせんべい）とも言う。瓦せんべいなどが代表的なものであり、八ツ橋のように米を材料とするものもある。甘味煎餅とも呼び、これは唐菓子の伝統を受け継いでおり（京都には「唐板」もしくは「唐板煎餅」といわれる、小麦粉・卵（後年に加わった？）砂糖から作られる煎餅がある）。また、北海道根室市や長崎県平戸市のオランダせんべいのように洋菓子であるワッフルの原料・製造法から創作された物もある。青森県南部地方発祥の南部せんべいは、基本は小麦粉と塩だけの素朴な煎餅である。馬鈴薯などのでん粉を用いるものとしては、愛知県の知多半島の名物となっている海老煎餅などがある。これは、でん粉に魚や海老の乾燥品を混ぜて焼いたもの。塩辛い味が基本だが、現在ではわさび味、カレー味、キムチ味など、さまざまな味の物が作られている。
九州などでは煎餅を「せんべい」でなく「せんぺい」と半濁音で発音する人もいる。にわかせんぺい（二○加煎餅）、九十九島せんぺい、湯せんぺいといった例がある。場合によっては醤油味・塩味を「せんべい」、甘いものを「せんぺい」と呼び分ける例もある。
