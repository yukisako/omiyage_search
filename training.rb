お供え菓子の一例
京都の和菓子は、宮中や公家、寺社、茶家に納めたり、特別なお祝いのためにあつらえる「上菓子」、普段に食べる「おまん（饅頭の略）」や「だんご」「餅菓子」に分けられる。前者を作る者を菓子匠、御菓子司などと称し、後者を作る者を「おまんやさん」「おもちやさん」と呼んだ。「○△餅」という店でも、饂飩（うどん）・寿司・おはぎが出されるところが現在もある。現代ではその区分も曖昧になってきている。上菓子は、お供え菓子や、茶道の菓子として洗練した発展を遂げ、普段の菓子も年中行事ごとに様々なものが食べられた経緯から多彩に展開した。その伝統が今日の京菓子に反映されている。
上菓子[編集]
上菓子は以下のような素材、中間素材、製法を用い、美的に作り上げる。
こなし
白こし餡（手亡豆等の隠元豆、あるいは白小豆の餡）と薄力粉を混ぜて蒸したものに砂糖水を加え、練り上げたもの。色をつけてさまざまな形に加工する。梅の蕾をかたどった「未開紅」、紅葉に仕立てた「竜田川」をはじめ、葛菓子の餡など多彩に展開する。
きんとん
蒸した山芋を裏漉しして砂糖と炊いたもの（薯蕷煉り切り）や、白餡を寒天で固めたもの（きんとん餡、天餡）、白餡を求肥でつないだもの（煉り切り）を、色々な色にそめ、うらごし器でそぼろ状にし、餡などの芯に植え付けて季節を表現する。　
求肥（ぎゅうひ）
もち米の粉を水で練って湯がき、火の上で砂糖を加えて練ったもの。夏の菓子「鮎」、「調布」などに使う。
くず
本葛粉に水を加えたものを漉して、砂糖を加え加熱し、アルファ化させる。葛切り、葛饅頭など透明感が涼しさを呼ぶ。また六方を焼いただけの「葛焼」は熟練を要する菓子。
薯蕷（じょうよ）
山芋のこと。「織部まんじゅう」など上用饅頭の皮は、山芋をすり下ろして砂糖と上用粉（細目の米粉）を加えたもの。餡を包んで蒸して作る。また、すり下ろした山芋に、砂糖、水、軽羹粉（粗目の米粉）を加え、蒸し上げたのがカルカン（軽羹）。蒸した山芋を裏漉しして砂糖と炊いたものが、薯蕷煉り切り。どの場合も、山芋本来の「白さ」と、独特の香りを生かすことが大切。
この他にも「道明寺」「淡雪」「錦玉」など中間素材は数多い。中間素材の段階までに炊く、蒸す、混ぜる、練るなどの作業があり、その一つでも蔑ろにすると美味しい菓子はできない。また材料も厳選されたものを素材に応じてあく抜きなどをしながら、味を引き出す技術が要求される。そして最後に季節感や、菓子が食べられる場のコンセプトを表現しなければならない。繊細な感覚と確かな技術で上菓子は作られる。ただし、その製法および感性は菓子店、職人によって千差万別であり、僅かな差異がそれぞれの個性になっている。対象として安価な駄菓子がある。
和菓子商人の受領官名[編集]
江戸時代、市内の菓子商人は店ごとに掾国名を付記したが、これは菓子商人は京都、中御門家の支配に属していたからである。当家の役所からは1年おきに下役数人ずつが浅草新堀端の松平西福寺内の役宅に出張し、江戸市内において掾国号などの官名を明記せずに、単に「XX屋XX兵衛」のような通称で営業する菓子屋を捜索した。そしてその家主、地主に差紙を送り、無免許者を役宅出張所に召喚し、菓子商人は京都、中御門家の支配に属すること、当家から掾国号の官名を受け営業すべきことを諭し、希望に応じて掾国名を授与した。
官名等級は藤原姓が最高で、次いで山城、大和、河内、和泉、摂津など、また武蔵、紀伊、尾張、常陸などは決して許されず、その他の国名およびXX堂、XX軒などの順位であった。これは俗に「餅屋官」とも言われ、大掾の階級はのちの奏任官待遇に該当し、正七位に相当するという。
