これら小麦あられシリーズを考えていく中で、孝は瀬戸内海の海辺で小エビが干してあるのを見て「鮮度の良い生エビを丸ごと使えば風味が良く健康にもいいはず」と入れることを思いつき[4]、味大将に初めて入れる。そして、1964年、かっぱあられ味大将とかっぱの一番槍の特徴を合わせた、小麦あられシリーズ27番目の最後の商品として「かっぱえびせん」が誕生した[5][6][7]。
かっぱえびせんも他の小麦あられシリーズと同様に当初は売れなかった。孝の3男でカルビー第3代社長である松尾雅彦は本格的に売れ始めたのが1966年と証言している[4]。「ニッポン・ロングセラー考 Vol.010 かっぱえびせん」（『COMZINE』NTTコムウェア株式会社、2004年）によれば、1965年には同社で最も売れる商品になっていたという。一方、『読売新聞』1998年10月11日大阪朝刊33面によると、1968年に後述のキャッチコピーを使ったCMを流すまでは、他の小麦あられと比べても特に売れる商品ではなかったという。
当時地方のベンチャー企業だったカルビー製菓にとって販路拡大が問題となった[5][4]。1967年8月、孝は”海外で話題になれば東京の流通も動かせるだろう”とアメリカ合衆国で開かれた国際菓子博覧会に出品する[8][補足 1]。1970年、同社はアメリカ合衆国に進出し、かっぱえびせんの現地生産を開始[10]、2008年時点でかっぱえびせんは、同国のほか中国、タイ王国などで製造・販売が行われている[11]。
1969年のテレビCMで登場した「やめられない、とまらない」というキャッチコピー[補足 2]が当たり、かっぱえびせんはカルビー製菓の売上高を急激に押し上げる人気商品となった[8]。
2014年に発売開始50周年を迎えた。同年6月、かっぱえびせんの原点となる「かっぱあられ」シリーズの内、「味王将」と「かっぱの一番槍」の2商品が期間限定で復刻販売された。
2015年、カルビー広島工場近くにゆめタウン廿日市が開店しこの中に日本で初めてとなるかっぱえびせん専門店「スナックキッチン My Calbee」をオープン、できたてのかっぱえびせんや後述の「かっぱえびせん 匠海(TAKUMI)」を販売している[12][13]。
フレーバー[編集]
1986年からシリーズ展開を開始し、通常の塩味以外にもフレンチサラダ味、ごま油風味、えびちりソース味、さくさくチーズ味、おつまみ海老、ピリピリマヨネーズ味、ローストガーリック味、辛一味醤油味、松茸風味、韓国のり味、香港バーベキュー味、タイスパイシーヤンミー味、タイトムヤム味、紀州の梅 焼きのり風味、本わさび味、辛口たまりしょうゆ味、荒挽きえびせん、柚子こしょう味、お茶づけ海苔味（永谷園との共同開発）、都こんぶ味（中野物産の都こんぶとのコラボ）、ごはんですよ味（桃屋海苔佃煮とのコラボ）など数多くのフレーバーが発売された。
2007年の9月下旬からは、ネット投票で再発売の要望が多かった2種類、「青じそ風味」「じんわり唐辛子」について期間・数量限定で再発売する[14]。
2003年からは、幼児向けに油を使用せず、塩分を半分に減らした「1才からのかっぱえびせん」が発売されている。
この他、発売地域に合わせた地域限定品のかっぱえびせん（例：関西どろソース味、九州甘口しょうゆ味）も存在する。
なお最高級のかっぱえびせんは、「かっぱえびせん 匠海(TAKUMI)」という商品名で、年数回、通信販売限定で発売されている。かっぱえびせんの原点である「瀬戸内海産の海老」をふんだんに練りこみ、調味料として藻塩を使ったもので、広島工場で限定製造されている。
海外展開と類似商品[編集]
海外では、中華人民共和国本土と香港で「蝦條」（普通話xiātiáo、広東語 ha1tiu4）の名で販売され、おなじみの商品となっており、広東省汕頭市に香港企業と合弁の工場も建てられた。一般名詞的な命名のためか、中国、台湾などで作られた、同じ名前のコピー商品も数多く出回っている。
日本では、天狗製菓が「えび天スナック」という、原材料や形状が類似した製品を販売している[15]。
韓国では「セウッカン」（새우깡、「えびせん」の意）というパッケージまでそっくりのコピー商品がある。農心が1971年12月に韓国で販売開始したもので、日本の一部の韓国食材店でも販売されている。味はかっぱえびせんよりやや薄口だが似ている。カルビーがパッケージに「カルシウム入り」や「ノンフライ」の表示を青色の濃淡の楕円形などで表示するとそれに合わせて、よく似たデザインを追加するなどの徹底ぶりである。2000年には、唐辛子味の「メウン セウッカン」（매운 새우깡、「辛いえびせん」の意）が発売された。中国にも工場があり生産販売されているが、2008年3月17日に「ノレバン セウッカン（노래방 새우깡、「カラオケ（パーティ用の）えびせん」の意）に鼠の頭が混入しているのが発見され、製品を回収する事件があり、中国の工場で混入したと見られている。その際日本で輸入業者経由の商品が一時輸入停止状態になった。
また、タイにはfriendship社の「Hanami (ฮานามิ) 」、フィリピンにはリウェイウェイ・ホールディングスの「Oishi」という商品が、それぞれ存在する。いずれも、日本のかっぱえびせんのパッケージに類似しており、両国でそれぞれ販売、一般化している。
