概要[編集]
1955年に発売されたスナック菓子「かっぱあられ」に由来する。漫画家の清水崑の代表作『かっぱ天国』のキャラクターのカッパをパッケージに起用したためにこの名称となった。かっぱあられの27番目の派生商品として、1964年に量り売り用一斗缶入りで「かっぱえびせん」を日本国内で発売開始。一斗缶入りでは湿気を吸いやすかったため売り上げは芳しくなかったが、湿気を防ぐため袋入りにしたところ人気商品となった。[1]
原料として使用されている小エビは当初は瀬戸内海産だったが、現在はグリーンランド近海・中国・日本産のアカエビ・サルエビ・キシエビ・ホッコクアカエビの4種類で、製造時の漁獲量によって種類ごとの割合が変化する。なおパッケージに描かれているエビは一般的なエビのイメージを元にデザインされたもので、特に種の特定はされず、愛称も付けられていない[2]。
歴史[編集]
カルビー創業者の松尾孝はエビの天ぷらが好物で、少年の頃は生まれ故郷の広島市内の川で捕ったエビをかき揚げにして食べていた。これが中にエビを入れる発想の原点である[3][4]。
太平洋戦争後の食糧難の時代、孝は「健康にいい、栄養のあるお菓子をつくる」[5]をモットーに製菓業を営んでいたが1953年に倒産、1955年カルビー製菓として再スタートした[6]。経営再建を目指していた中で当時配給制で値段が高かった米に代わりアメリカから大量に輸入されていた小麦に着目し、日本で初めて小麦粉を使ってあられを作り水飴でコーティングし甘く仕上げ、1955年「かっぱあられ」として発売した[6][5]。その後も研究を重ね、様々な小麦あられを発売したものの、思うようには売れなかった[5]。
