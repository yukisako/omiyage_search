種類[編集]

煎餅屋
日本の米を原料とする煎餅は『米菓煎餅（べいかせんべい）』ともいい、粳米（うるちまい）をつぶしたり搗（つ）いたりして延ばしたものを焼いてつくる米菓であり、これは「焼きせんべい」と「揚げせんべい」に分ける事ができる[1]。醤油や塩による味付けをしたものが多く、煎餅を焼いて売る『煎餅屋』もみられる[2]。 南部煎餅は小麦粉を主原料とする。
歴史[編集]
日本で「煎餅」の記述が見られるのは正倉院所蔵の737年頃の文書であるが、ここに登場する「煎餅」は現在のものと違い、小麦粉を油で固めたいりもち（煎餅）であり、現在と同じ煎餅は草加せんべいが初であるとされている[3]。
草加せんべい[編集]

草加せんべい発祥の地の碑
現在の煎餅の一番古い物は、日光街道の2番目の宿場町だった草加宿（現在の埼玉県草加市）で団子屋を営んでいた「おせん」という老婆が、ある日、侍に「団子を平らにして焼いたらどうか」と言われて始めたのが名前の由来、というよく知られた話がある。
草加宿一帯の農家では、蒸した米をつぶして丸め、干したもの（「堅餅」という）に塩をまぶして焼き、間食として食べていた。草加宿が日光街道の宿場町として発展したことに伴い、この塩味の煎餅が旅人向けの商品として売り出され、各地に広まった。その後、利根川沿岸（千葉県野田市）で生産された醤油で味をつけるようになり、現在の草加煎餅の原型となったとされている。これは船によって江戸に伝えられ広く伝わっていった。
吉野ヶ里遺跡や登呂遺跡の住居跡からも一口大程度に平たく潰し焼いた穀物製の餅が出土しており、既に弥生時代には煎餅に近い物が食されていたのではないかと考えられている。さらに時代を遡り、すりつぶした栗や芋類（サトイモ、ヤマイモなど）などを同様に一口大程度に平たく押しつぶして焼いた物はさらに以前の縄文遺跡の住居跡からも出土している。また、台湾や東南アジアでは同様のタロイモ煎餅が存在し、ミクロネシア・ポリネシア・メラネシアといったヤムイモやタロイモを主食とする環太平洋諸島にはタロイモやヤムイモを原料とする煎餅に近い食品が存在する。
別説で、日光街道草加松原の茶屋において売られていた団子を「焼き餅にして売ったらどうか」と提案され、それが名物となった、という話もあるが定かでは無い[4]。
中国の煎餅[編集]

中華圏の煎饼
「餅」という字は、もともと中国では主に小麦・粟・緑豆などの粉を水で練って、平たく成形した食品全般を指す。そのため、煎餅は餅米を搗いて作る餅（もち）を基本的に使っていないが、「餅」という字が入っている。
また、「煎」も、中国では薬を煎じるという意味ではなく、鉄板で焼くことを指す。つまり、中国の「煎餅（ジエンピン jiānbǐng）」は小麦粉などの粉を水で練って、鉄板で焼いたものである。別名「薫火」とも呼ばれる。中国においては、現在も山東煎餅、天津煎餅など、一銭洋食に似た作り方の軽食が作られている。日本のお好み焼きもこれらの「煎餅」の一種として「日式雜菜煎餅」などと呼ばれることがある。逆に、日本の揚げ煎餅の類を「仙貝 シエンベイ xiānbèi」と音訳で呼ぶ場合がある。中国の煎餅は、いずれも軟らかくて、巻いたり、曲げたりして食べることが可能なものであり、形状はクレープやドーサに似ている。
