和菓子

見た目の美しさも追求される和菓子
和菓子（わがし）とは、日本の伝統的製造法で作られた菓子のこと。明治時代以降にヨーロッパなどから新しく日本に入ってきた洋菓子に対して使われる言葉[1][注釈 1]。言葉として定着したのは第二次世界大戦の後であり、国語辞典などにも登場し始めた[1]。
遣唐使によって伝来した唐菓子や、宣教師によってもたらされた南蛮菓子も和菓子に含めるとする意見が主流である。
