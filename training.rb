歴史[編集]
江戸時代初期に生まれた鶉餅（うずらもち）が1771年（明和8年）に江戸・小石川の未亡人により小形化され、餡に砂糖を加えられたものが始まりとされる。腹持ちがよいことから腹太餅（はらぶともち）、大腹餅（だいふくもち）と呼ばれていたものが、吉字を使った大福餅に変化したという。寛政の改革時には、行商が焼きながら売り歩く熱い大福が流行した[1]。
