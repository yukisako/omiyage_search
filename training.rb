ポテトチップス (potato chips) とは、薄切りにして冷水で短時間さらした後高温の油で軽く色づくまで揚げたじゃがいものことである[2]。保存が利き、冷めても美味しい。それを塩や香辛料で味付けしたスナック菓子のこと。ポテトチップあるいは、クリスプス (Crisps) とも呼ばれる。日本ではポテチ、チップスなどと省略される場合がある。
