チョコレート（英: chocolate）は、カカオの種子を発酵・焙煎したカカオマスを主原料とし、これに砂糖、ココアバター、粉乳などを混ぜて練り固めた食品である。略してチョコともいう。ショコラ（フランス語: chocolat）と呼ばれることもある。
明治時代の独和辞典『袖珍獨和新辭林』によれば、Schokolateに楂古聿（ショコラ、チョコレート、日本語読みでサコイツ、サコイチ）という訳を当てている。
近年の工業生産チョコレートでは、カカオマス、砂糖、ココアバター、粉乳といった主要材料以外に、原料コスト削減や加工性[1]を上げる目的で植物性の油脂などを加えたり、加工コスト削減の目的で乳化剤などを加えたり、風味の向上の目的で香料や甘味料などを加えるなど、様々な添加物が配合されることも多い。
