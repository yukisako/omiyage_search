月寒あんぱん[編集]
北海道札幌市豊平区月寒では明治時代後期に、木村屋のあんパンの話を元に「月寒あんぱん」を作り出したが、製法や実物などの情報がなかったため、パンというよりも月餅に近いサイズと食感を持ったものとなった。当時の陸軍歩兵第25連隊の兵士にとって、重労働の後のエネルギー源としてもてはやされ、それによって出来た道路に「アンパン道路」と名付けるほどだった。現在では、ほんまが製造し、道外でも販売されている。
川口あんぱん[編集]
青森県北津軽郡板柳町には、「川口あんぱん」と呼ばれる小麦粉を原料としたカステラ風の生地で白あんを包んだ饅頭状の和菓子が存在する。これは明治初期（1880年）に考案されたといわれるが、月寒あんぱん同様、名称以外に詳細な情報が存在しなかったため、既存の製菓技術を用いてオーブンで仕上げた焼き菓子になったと見られる。
酒種あんぱん[編集]
大分県由布市で、日本酒を造るときの酵母菌（酒種）を独自に純粋培養してパン生地に混ぜ込んで作られている、日本人になじみの深い酒種あんぱん。酵母菌を144時間ねかせて低温発酵で超熟した酒種あんぱんを完成させるまでに15年余の歳月がかかっているとのこと（製法特許第3701959号）。粒あん、こしあん、黒豆入り白あんの3種類がある。麹は黒麺を使用している。
