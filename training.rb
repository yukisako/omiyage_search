起源説等[編集]
マントウの起源については饅頭 (中国)#起源説話を参照
日本の饅頭の起源には2つの系統がある。ひとつは1349年（南朝：正平4年、北朝：貞和5年）に禅宗の僧と一緒に中国から渡来した林浄因と共に入ってきたと言われている。その際、禅宗のお茶と一緒に食べる菓子として饅頭を用いる事を考えた。しかし従来の饅頭は肉を使ってしまう（本来の仏教では肉食は禁じられていないが、日本では肉食を禁じた）ので、代わりに小豆を使った饅頭が考案されたと言われている。その後、林は奈良の漢國神社の近くに住居して塩瀬という店を立てたことから、漢國神社内の林神社と呼ばれる饅頭の神社で、菓祖神として祀られている[1]。
もうひとつの系統は、林が伝えたとされる年より100年ほど遡る1241年（仁治2年）に南宋に渡り学を修めた円爾が福岡の博多でその製法を伝えたと言われる。円爾は辻堂（つじのどう=現・博多駅前一丁目）に臨済宗・承天寺を創建し博多の西、荒津山一帯を托鉢に回っていた際、いつも親切にしてくれていた茶屋の主人に饅頭の作り方を伝授したと言われる。このときに茶屋の主人に書いて与えた「御饅頭所」という看板が、今では東京・赤坂の虎屋黒川にある。奈良に伝わった饅頭はふくらし粉を使う「薬饅頭」で、博多の方は甘酒を使う「酒饅頭」とされる。
伝来当時は現在の饅頭につながる甘い饅頭と、主として野菜を餡とした菜饅頭の二種類が存在していた。後者は現在の肉まんに近い物と考えられているが、仏教の影響もあって、近在以前の日本ではもっぱら野菜が餡として用いられた。仏教寺院ではいわゆる点心（ここでは軽食や夜食）の一種類とみなされ、軽食として用いられていた。しかし、米飯や麺類が主食として存在し、とくに麺類（うどん、そば、素麺など）が早くから軽食として存在した一般社会では、製法の煩雑さなどからほとんど定着せず、甘い饅頭や麺類のように菜饅頭を専門の業者が製造する事もなかった。ただ、寺院における食事の記録には記載されている事が多く、江戸時代に入っても『豆腐百珍』に「菜饅頭」として製法が記載されている事から、寺院等では軽食として長い間食べられていたようである。
また、直接的な饅頭の由来ではないものの、江戸時代以降に南蛮菓子や中国菓子の製法として焼き菓子の製造技術が日本に伝播した。この技術が饅頭にも応用され、焼き饅頭と呼ばれる日本独特のジャンルが生まれた。
日本に定着した後、餡や皮の製法にさまざまな工夫が凝らされ種々の饅頭が作られるようになった。
しかし、肉まんや餡まんなど、従来の饅頭（マントウ）を起源とした厚めでふわっとした皮の中華風の饅頭（包子、パオズ）は、甘いものであっても一般的に中華まんとして区別されている（特殊な事例として愛媛県の労研饅頭があり、これもマントウを起源とするが（同項参照）、こちらは和菓子の分類に入れられる事が多い）。中にはカレーまん、ピザまん、バナナまんなど、中国には存在しない中華まんもある。 日本風の焼いて作る饅頭は台湾でも「日式饅頭」、「日本饅頭」と称して製造されている。
和菓子としての饅頭[編集]
生地の種類[編集]
茶饅頭
基本は小麦粉、黒砂糖、膨張剤を用いた生地に餡子を包んだ饅頭だが、各地にさまざまなバリエーションがある。利休饅頭（大島饅頭）、温泉饅頭、薄皮饅頭などとも呼ばれ、観光地（特に温泉街）でのおやつやお土産にも好んで用いられる。
薯蕷饅頭（じょうよまんじゅう）
すりおろした薯蕷（ナガイモ）の粘りを利用して米粉（薯蕷粉、上新粉）を練り上げ、その生地で餡等を包んでしっとりと蒸し上げた饅頭。上用饅頭とも言い、紅白饅頭や織部まんじゅうなどがこれにあたる。使われる薯蕷にはつくね芋（京都地方）、大和芋（関東）、伊勢芋（中部地方）などがある。茶席で使われる主菓子（おもがし）のひとつ。奈良の林浄因が作ったという言い伝えから、その子孫のお店の名前をとって「塩瀬饅頭」とも呼ばれる（京菓子#代表的な京菓子も参照）。
酒饅頭
酒母（酒種、麹に酵母を繁殖させたもの）を使って小麦粉の生地を発酵させ、中に餡を入れた饅頭。虎屋饅頭とも呼ばれる。「酒饅頭」は長野市、新潟県長岡市ではあんまんのようなものであり、福井県三国では形状は平たくなり焼き印を入れてあり、岐阜県大垣市のものは茶饅頭のようなものであるなど地域によって形状、味覚、製法が異なる。酒饅頭を氷水で浸したものを水饅頭と形容する場合もある (後述) 。あんパンのアイデアの基になった。
塩饅頭
焼饅頭
主にカステラ風の生地で餡を包んだ焼き菓子の一種。唐饅頭、もみじ饅頭、栗饅頭、千鳥饅頭（福岡県）、乳菓、中華饅頭などがこれにあたる。洋菓子や中華菓子（月餅）の影響を受けて明治時代以降に発達したとされる。オーブンで焼く物（オーブン物）、鉄板で焼く物（平鍋物、平物）などがある。さらにパイ皮やビスケット生地、スコーン生地を用いたより洋菓子に近い物（宮崎県のチーズ饅頭など）がある。また、長崎県の一口香や北海道のわかさいものように独特の製法の物もある。
栗饅頭
皮に卵黄を塗って焼き、栗の皮の色に似せたもの。中身は白あんだが甘味に栗の甘露煮で用いた蜜を使ったり、栗そのものを混ぜ込んだりしている。

水饅頭
水饅頭
くず粉を用いて作った透明の生地で餡を包んだ夏季の生菓子。水仙饅頭とも言う。そのまま器に盛って食べるのが一般的だが、冷水に浸して食べるものもある。「水饅頭（水まんじゅう）」の名称では岐阜県大垣市や奈良県御所市のものなどが知られるが、全国的には「葛饅頭」の名称のほうが一般的である。映画『聯合艦隊司令長官 山本五十六』(2011年作品)において山本五十六の好物として酒饅頭を氷水に浸したデザートを「水饅頭」として紹介している。
麩饅頭
小麦粉の皮の代わりに生麩で餡を包んだ生菓子。笹の葉で巻く事が多い。単に生麩とも。
味噌饅頭
小麦粉に味噌を練りこんで蒸したもの。身延饅頭がこの部類にあたる。餡の甘みと味噌の辛味がうまく合っている。
静岡県浜松市の細江から引佐近辺では、黒糖を用いた小麦粉の皮で黒餡を包んだ茶饅頭の一種を、色が味噌に似ているため「みそまんじゅう」（あるいは「みそまん」）と呼ぶ。
その他
マシュマロ生地を用いた物（福岡県の鶴乃子）、ういろう生地の物（大分県中津市のういろう饅頭など）、湯で練った米粉生地を用いた物（鹿児島県の伊集院饅頭、岩手県のぶちょうほう饅頭など）、かるかん饅頭、落雁生地の物（兵庫県赤穂市の塩味饅頭など）などがある。
